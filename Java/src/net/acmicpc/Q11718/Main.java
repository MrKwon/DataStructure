package net.acmicpc.Q11718;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner stdIn = new Scanner(System.in);

        while (stdIn.hasNextLine()) {
            String str = stdIn.nextLine();
            System.out.println(str);
        }
        stdIn.close();
    }
}
